def write_gpu_comparison():
    """Why no GPU baseline — reviewer-ready argument."""
    tex = r"""% Auto-generated by generate_narrative_content.py
% GPU comparison argument — addresses reviewer concern about missing GPU baseline
% Insert in Section V (Evaluation) or as a paragraph in Related Work

% Option A: Paragraph form (recommended for space-constrained papers)
We do not include a GPU baseline because MiniRocket's computational structure
is fundamentally different from deep learning workloads that GPUs are optimized for.
MiniRocket uses 84 fixed, non-learned convolutional kernels with ternary weights
$\{-1, +2\}$ followed by a linear Ridge classifier---there are no weight updates,
no backpropagation, and no batch matrix multiplications.
A GPU implementation would reduce to a memory-bound scatter-gather over 84 short
(9-tap) filters with irregular dilation patterns, which does not map efficiently
to GPU SIMT execution.
Prior work on time series classification consistently reports that
kernel-method accelerators (MiniRocket, ROCKET) show marginal GPU speedup over
optimized CPU implementations~\cite{dempster2021minirocket,tan2022multirocket},
with the GPU advantage emerging only for deep models (InceptionTime, ResNet)
where batch GEMM dominates.
Our C++ baseline with \texttt{-O3 -march=native} represents the realistic
deployment target for non-learned classifiers.

% Option B: Footnote form (if space is very tight)
% \footnote{We omit GPU baselines: MiniRocket's 84 fixed ternary kernels
% with irregular dilations are memory-bound on GPUs, unlike deep learning
% workloads. Prior work~\cite{dempster2021minirocket} confirms marginal
% GPU speedup for kernel methods vs.\ optimized CPU.}
"""
    path = os.path.join(TABLES_DIR, "gpu_comparison.tex")
    with open(path, 'w') as f:
        f.write(tex)
    print(f"Written: {path}")


