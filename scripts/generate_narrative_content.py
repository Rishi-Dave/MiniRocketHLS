def write_classifier_description():
    """Ridge classifier description — addresses reviewer concern about missing details."""
    tex = r"""% Auto-generated by generate_narrative_content.py
% Ridge classifier description — addresses reviewer request for classifier details
% Insert in Section III (Architecture) after feature extraction description

\subsection{Classification Stage}

After feature extraction produces an $F$-dimensional feature vector
(where $F = 84 \times D \times P$ for $D$ dilations and $P$ pooling operators),
two additional pipeline stages complete the inference:

\textbf{Scaler Kernel (K2).}
A StandardScaler normalizes each feature $f_i$ to zero mean and unit variance:
$\hat{f}_i = (f_i - \mu_i) / \sigma_i$, where $\mu_i$ and $\sigma_i$ are
precomputed from training data and stored in on-chip BRAM.

\textbf{Classifier Kernel (K3).}
A Ridge regression classifier computes decision scores
$s_c = \mathbf{w}_c^T \hat{\mathbf{f}} + b_c$ for each class $c$,
selecting $\arg\max_c\, s_c$.
Ridge regression is a linear classifier with $L_2$ regularization,
chosen because it matches the feature extraction's
``transform once, classify linearly'' paradigm without iterative optimization.
The coefficient matrix $\mathbf{W} \in \mathbb{R}^{C \times F}$ and
intercept vector $\mathbf{b} \in \mathbb{R}^C$ are precomputed on the host
and transferred via HBM.

K2 and K3 are connected to K1 (feature extraction) via AXI-Stream interfaces,
forming a three-stage pipeline.
For MiniRocket ($F = 840$), K2 and K3 together consume $<$0.1\,ms per sample,
making K1 the dominant bottleneck (85--99\% of total inference time).
"""
    path = os.path.join(TABLES_DIR, "classifier_description.tex")
    with open(path, 'w') as f:
        f.write(tex)
    print(f"Written: {path}")


def write_streaming_reframe():
    """Streaming motivation — reframes streaming as future work / SmartNIC architecture."""
    tex = r"""% Auto-generated by generate_narrative_content.py
% Streaming reframe — positions streaming as motivation, not headline contribution
% Insert in Section I (Introduction) or Section VI (Discussion)

% Introduction paragraph (motivation):
Time series classification is increasingly deployed at network ingestion points---
sensor gateways, industrial PLCs, and data center SmartNICs---where data arrives
as a continuous stream and must be classified with minimal latency before storage.
Traditional CPU-based classifiers introduce milliseconds of latency per sample
and require dedicated server resources.
FPGA-based SmartNICs offer a compelling alternative: by co-locating the classifier
with the network interface, classification can occur \emph{in the data path}
without PCIe round-trips or host CPU involvement.

% Architecture paragraph (Section IV or new subsection):
To demonstrate streaming feasibility, we implement a 100G SmartNIC prototype
using the Xilinx CMAC IP and a UDP network layer on the Alveo U280.
The MiniRocket inference kernel connects directly to the network stack via
AXI-Stream interfaces, enabling end-to-end classification of network-delivered
time series without host memory copies.
This architecture achieves wire-rate classification for time series up to
length 8,192 at the kernel's native throughput, with the CMAC and network
layer adding negligible latency ($<$1\,$\mu$s).
The streaming configuration uses identical feature extraction logic to the
PCIe-attached variant, ensuring bit-exact accuracy.
"""
    path = os.path.join(TABLES_DIR, "streaming_reframe.tex")
    with open(path, 'w') as f:
        f.write(tex)
    print(f"Written: {path}")


